'use client';

import { useEffect } from 'react';
import styles from './HelpModal.module.css';

interface HelpModalProps {
  onClose: () => void;
}

export default function HelpModal({ onClose }: HelpModalProps) {
  useEffect(() => {
    const handleKey = (e: KeyboardEvent) => {
      if (e.key === 'Escape') onClose();
    };
    document.addEventListener('keydown', handleKey);
    return () => document.removeEventListener('keydown', handleKey);
  }, [onClose]);

  return (
    <div className={styles.overlay} onClick={onClose} role="dialog" aria-modal="true" aria-label="Справка">
      <div className={styles.modal} onClick={e => e.stopPropagation()}>
        <button className={styles.closeButton} onClick={onClose} aria-label="Закрыть">×</button>
        <h2 className={styles.title}>Как пользоваться сервисом</h2>

        <section className={styles.section}>
          <h3>Что делает сервис</h3>
          <p>True AML проверяет TRON-адреса и транзакции на риски отмывания денег (ОД) и финансирования терроризма (ФТ). Система анализирует историю транзакций, связи с известными рисковыми адресами и источники поступающих средств.</p>
        </section>

        <section className={styles.section}>
          <h3>Проверка адреса: шаги анализа</h3>
          <p>Для кошелька цепочка примерно такая (сверху вниз — по порядку в модели):</p>
          <ol>
            <li>
              <strong>Сверка с рисковыми справочниками.</strong> Адрес проверяется по внутреннему чёрному списку и внешним меткам безопасности (например, фишинг
              или взлом контракта). При сильном прямом сигнале ответ может быть сформирован без полного обхода графа.
            </li>
            <li>
              <strong>Контекст в сети.</strong> Подтягиваются сведения об аккаунте: обычный кошелёк или смарт-контракт, при необходимости — дополнительные признаки
              вроде активности пулов ликвидности.
            </li>
            <li>
              <strong>Поведение по транзакциям TRON.</strong> Загружается выборка обычных транзакций: сколько операций, как давно кошелёк в сети, паттерны
              (частота, входящие/исходящие потоки). Это нужно для <em>поведенческой</em> части оценки и объёма, а не для процентов «откуда пришли стейблкоины».
            </li>
            <li>
              <strong>Базовый риск и флаги.</strong> Из прямых сигналов (чёрный список, метки безопасности, эвристики по паттернам) считается стартовый балл и
              набор флагов модели.
            </li>
            <li>
              <strong>Стейблкоины USDT/USDC (TRC-20).</strong> Отдельно разбираются <em>входящие</em> переводы по контрактам USDT и USDC: от кого пришли средства,
              объёмы, связь с рисковыми источниками с учётом глубины цепочки. Именно эти данные дают <strong>заражённость</strong> и <strong>источники средств</strong>{' '}
              (надёжные / подозрительные / опасные). Если входящих стейблкоин-переводов в выборке нет, блок SoF показывается пустым — это не «100% подозрительно».
            </li>
            <li>
              <strong>Контрагенты притока.</strong> Выделяются основные отправители стейблкоинов с объёмом, локальной оценкой риска и типом сущности (биржа, миксер и
              т.д.), чтобы было видно структуру притока.
            </li>
            <li>
              <strong>Итог и смягчения.</strong> Компоненты (базовый риск, заражённость, поведение, объём) собираются в итоговый балл 0–100 и уровень риска; для
              адресов из сильного белого списка (известные биржевые депозиты) итог может быть сильно снижен. Добавляются текстовые пояснения. Краткая информация о
              проверке может сохраниться во внутреннем профиле адреса для истории.
            </li>
          </ol>
        </section>

        <section className={styles.section}>
          <h3>Оценка риска (0–100)</h3>
          <p>Числовой показатель риска адреса или транзакции:</p>
          <ul>
            <li><strong>0–24</strong> — <span className={styles.low}>НИЗКИЙ</span>: нет явных признаков риска</li>
            <li><strong>25–49</strong> — <span className={styles.medium}>СРЕДНИЙ</span>: есть некоторые подозрительные факторы</li>
            <li><strong>50–79</strong> — <span className={styles.high}>ВЫСОКИЙ</span>: значительные признаки риска, требует внимания</li>
            <li><strong>80–100</strong> — <span className={styles.critical}>КРИТИЧЕСКИЙ</span>: сильные признаки риска ОД/ФТ</li>
          </ul>
        </section>

        <section className={styles.section}>
          <h3>Заражённость (Taint)</h3>
          <p>Процент входящих стейблкоинов (USDT/USDC), которые прослеживаются до рисковых источников через цепочки переводов. Высокая заражённость означает, что значительная часть средств прошла через адреса из чёрных списков.</p>
        </section>

        <section className={styles.section}>
          <h3>Источники средств</h3>
          <p>Разбивка входящих USDT/USDC по трём категориям:</p>
          <ul>
            <li><strong>✓ Надёжные</strong> — биржи, известные сервисы с хорошей репутацией</li>
            <li><strong>⚠ Подозрительные</strong> — неизвестные кошельки, DeFi, P2P</li>
            <li><strong>● Опасные</strong> — адреса из чёрных списков: санкционные, мошеннические, миксеры, гемблинг</li>
          </ul>
        </section>

        <section className={styles.section}>
          <h3>Контрагенты</h3>
          <p>Топ адресов, взаимодействовавших с проверяемым кошельком. Показывает объём переводов, оценку риска контрагента и его тип (биржа, миксер и т.д.).</p>
        </section>

        <section className={styles.section}>
          <h3>Прямая связь с рисковым адресом</h3>
          <p>Означает, что в транзакции напрямую участвовал адрес из чёрного списка — отправитель или получатель.</p>
        </section>
      </div>
    </div>
  );
}
